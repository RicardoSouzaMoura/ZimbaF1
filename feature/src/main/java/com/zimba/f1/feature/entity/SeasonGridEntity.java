package com.zimba.f1.feature.entity;

public class SeasonGridEntity {

    private String driverChampionName;
    private String seasonYear;
    private String constructorChampionName;

    public String getDriverChampionName() {
        return driverChampionName;
    }

    public void setDriverChampionName(String driverChampionName) {
        this.driverChampionName = driverChampionName;
    }

    public String getSeasonYear() {
        return seasonYear;
    }

    public void setSeasonYear(String seasonYear) {
        this.seasonYear = seasonYear;
    }

    public String getConstructorChampionName() {
        return constructorChampionName;
    }

    public void setConstructorChampionName(String constructorChampionName) {
        this.constructorChampionName = constructorChampionName;
    }

}
